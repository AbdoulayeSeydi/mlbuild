def _model_info_from_path(path: Path) -> "ModelInfo":
    """
    Build a ModelInfo from any supported model file.

    ONNX  : load_model(path) → ModelIR → ModelInfo built from ir fields
    CoreML: extract_coreml_info(path) → ModelInfo
    TFLite: extract_tflite_info(path) → ModelInfo
    """
    suffix = path.suffix.lower()

    if suffix == ".onnx":
        ir = load_model(str(path))

        def _to_tensor_info(t) -> TensorInfo:
            shape = tuple(d.value for d in t.shape.dims) if t.shape is not None else None
            return TensorInfo(name=t.name, shape=shape)

        ops = ir.get_operators()
        op_types = set(ops.keys()) if isinstance(ops, dict) else set(ops)

        return ModelInfo(
            format      = "onnx",
            inputs      = [_to_tensor_info(t) for t in ir.input_tensors],
            outputs     = [_to_tensor_info(t) for t in ir.output_tensors],
            op_types    = op_types,
            node_count  = ir.num_nodes,
            param_count = ir.num_parameters,
        )

    elif suffix in (".mlmodel", ".mlpackage") or path.is_dir():
        return extract_coreml_info(str(path))

    elif suffix == ".tflite":
        return extract_tflite_info(str(path))

    else:
        pytest.skip(f"Unsupported model format: {path.suffix}")


def _run_build_profile(path: Path) -> "ModelProfile":
    """Full path → ModelProfile using the real API chain."""
    info   = _model_info_from_path(path)
    result = detect_task(info)
    return build_profile(info, result)


def _model_info_for_scoring(path: Path) -> "ModelInfo":
    """Get ModelInfo for an ONNX path (scoring fns require ONNX-derived info)."""
    if path.suffix.lower() != ".onnx":
        pytest.skip(f"Scoring tests require ONNX model, got {path.suffix}")
    return _model_info_from_path(path)


