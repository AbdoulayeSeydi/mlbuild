class TestBuildProfileInputTypes:
    """build_profile() accepts str paths as well as pathlib.Path objects."""

    def test_accepts_string_path(self, fixture_path):
        """Passing a str instead of Path must work identically."""
        path = fixture_path("B01")
        if path is None or not Path(path).exists():
            pytest.skip("B01 fixture not available")

        profile_str  = _run_build_profile(Path(path))
        profile_path = _run_build_profile(Path(path))

        assert profile_str.domain    == profile_path.domain
        assert profile_str.subtype   == profile_path.subtype
        assert profile_str.execution == profile_path.execution