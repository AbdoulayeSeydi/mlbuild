class TestUnimplementedSubtypeGuard:
    """SEGMENTATION subtype should be guarded — no silent failures."""

    def test_segmentation_in_unimplemented_set(self):
        assert Subtype.SEGMENTATION in _UNIMPLEMENTED_SUBTYPES

    def test_segmentation_profile_does_not_crash(self):
        """build_input_schemas_with_roles must not raise on SEGMENTATION."""
        info = ModelInfo(
            format="onnx",
            inputs=[TensorInfo(name="pixel_values", shape=(1, 3, 224, 224))],
            outputs=[TensorInfo(name="masks", shape=(1, 1, 224, 224))],
            op_types={"Conv", "MaxPool"},
        )
        result = _dummy_result(TaskType.VISION)
        profile = _make_profile(Domain.VISION, Subtype.SEGMENTATION)
        try:
            schemas, roles = build_input_schemas_with_roles(info, result, profile)
            # If it returns without raising, schemas must still be valid
            assert isinstance(schemas, list)
        except NotImplementedError:
            pass  # raising NotImplementedError is also acceptable
        except Exception as exc:
            pytest.fail(
                f"SEGMENTATION caused unexpected exception {type(exc).__name__}: {exc}"
            )

    def test_segmentation_schemas_are_not_empty_if_returned(self):
        """If SEGMENTATION doesn't raise, it must still return usable schemas."""
        info = ModelInfo(
            format="onnx",
            inputs=[TensorInfo(name="pixel_values", shape=(1, 3, 224, 224))],
            outputs=[],
            op_types={"Conv"},
        )
        result = _dummy_result(TaskType.VISION)
        profile = _make_profile(Domain.VISION, Subtype.SEGMENTATION)
        try:
            schemas, _ = build_input_schemas_with_roles(info, result, profile)
            assert isinstance(schemas, list)
        except NotImplementedError:
            pass


