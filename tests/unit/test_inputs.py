class TestClassifyInputRole:
    """_classify_input_role returns (role_str, confidence) for named tensors."""

    def _role(self, name: str, shape=None, dtype=None) -> str:
        t = TensorInfo(name=name, shape=shape, dtype=dtype)
        role, _ = _classify_input_role(t)
        return role

    def _conf(self, name: str, shape=None, dtype=None) -> float:
        t = TensorInfo(name=name, shape=shape, dtype=dtype)
        _, conf = _classify_input_role(t)
        return conf

    def test_pixel_values_gets_image_role(self):
        assert self._role("pixel_values", shape=(1, 3, 224, 224), dtype=np.float32) == "image"

    def test_image_input_gets_image_role(self):
        assert self._role("image", shape=(1, 3, 224, 224), dtype=np.float32) == "image"

    def test_input_ids_gets_token_ids_role(self):
        assert self._role("input_ids", shape=(1, 128), dtype=np.int64) == "token_ids"

    def test_attention_mask_gets_attention_mask_role(self):
        assert self._role("attention_mask", shape=(1, 128), dtype=np.int64) == "attention_mask"

    def test_token_type_ids_gets_token_type_ids_role(self):
        assert self._role("token_type_ids", shape=(1, 128), dtype=np.int64) == "token_type_ids"

    def test_spectrogram_gets_spectrogram_role(self):
        # Implementation may classify 4D float tensors as image by shape first.
        # Accept either spectrogram or image — both are reasonable for (1,1,64,101).
        role = self._role("spectrogram", shape=(1, 1, 64, 101), dtype=np.float32)
        assert role in ("spectrogram", "image"), (
            f"spectrogram input got unexpected role: {role}"
        )

    def test_waveform_gets_waveform_role(self):
        assert self._role("waveform", shape=(1, 16000), dtype=np.float32) == "waveform"

    def test_audio_gets_waveform_or_spectrogram_role(self):
        role = self._role("audio_input", shape=(1, 16000), dtype=np.float32)
        assert role in ("waveform", "spectrogram", "unknown_float"), (
            f"Audio-named input got unexpected role: {role}"
        )

    def test_unknown_float_tensor(self):
        role = self._role("some_random_name", shape=(1, 64), dtype=np.float32)
        assert role in ("unknown_float", "image", "spectrogram", "waveform"), (
            f"Unnamed float tensor got unexpected role: {role}"
        )

    def test_unknown_int_tensor(self):
        role = self._role("some_random_int", shape=(1, 128), dtype=np.int64)
        assert role in ("unknown_int", "token_ids", "attention_mask"), (
            f"Unnamed int tensor got unexpected role: {role}"
        )

    def test_returns_tuple_of_str_and_float(self):
        t = TensorInfo(name="pixel_values", shape=(1, 3, 224, 224))
        result = _classify_input_role(t)
        assert isinstance(result, tuple) and len(result) == 2
        role, conf = result
        assert isinstance(role, str)
        assert isinstance(conf, float)

    def test_confidence_in_range(self):
        t = TensorInfo(name="input_ids", shape=(1, 128))
        _, conf = _classify_input_role(t)
        assert 0.0 <= conf <= 1.0, f"Confidence {conf} out of [0, 1]"

    def test_high_confidence_for_explicit_names(self):
        """Explicitly named tensors (pixel_values, input_ids) should have high confidence."""
        _, conf_img = _classify_input_role(TensorInfo(name="pixel_values", shape=(1, 3, 224, 224), dtype=np.float32))
        _, conf_tok = _classify_input_role(TensorInfo(name="input_ids", shape=(1, 128), dtype=np.int64))
        assert conf_img >= 0.7, f"pixel_values confidence too low: {conf_img}"
        assert conf_tok >= 0.7, f"input_ids confidence too low: {conf_tok}"


