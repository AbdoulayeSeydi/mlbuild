def _ti(t) -> TensorInfo:
    """Convert ModelIR tensor to TensorInfo."""
    shape = tuple(d.value for d in t.shape.dims) if t.shape else None
    return TensorInfo(name=t.name, shape=shape)


def _load_info(path: str) -> ModelInfo:
    ir = load_model(path)
    ops = ir.get_operators()
    return ModelInfo(
        format="onnx",
        inputs=[_ti(t) for t in ir.input_tensors],
        outputs=[_ti(t) for t in ir.output_tensors],
        op_types=set(ops.keys()),
        node_count=ir.num_nodes,
        param_count=ir.num_parameters,
    )


def _onnx(prefix: str) -> str:
    """Resolve fixture prefix to full ONNX path, skipping if not found."""
    fx = Path("tests/fixtures/onnx")
    matches = [f for f in fx.iterdir() if f.name.startswith(prefix) and f.suffix == ".onnx"]
    if not matches:
        pytest.skip(f"Fixture {prefix} not found in {fx}")
    return str(matches[0])


def _make_profile(
    domain: Domain,
    subtype: Subtype = Subtype.NONE,
    execution: ExecutionMode = ExecutionMode.STANDARD,
) -> ModelProfile:
    return ModelProfile(
        domain=domain,
        subtype=subtype,
        execution=execution,
        confidence=0.9,
        confidence_tier="graph",
    )


def _dummy_result(task: TaskType = TaskType.VISION):
    from mlbuild.core.task_detection import DetectionResult, DetectionSignal, DetectionTier
    return DetectionResult(
        primary=task,
        tasks={task},
        signals=[DetectionSignal(task, DetectionTier.GRAPH, 3.0, "test")],
        tier=DetectionTier.GRAPH,
    )


